A linked list is a fundamental data structure used in computer science to store a sequence of elements. Unlike arrays, linked lists do not store elements in contiguous memory locations; instead, each element (called a node) contains the data and a reference (or pointer) to the next node in the sequence.

Here’s a breakdown:

Structure of a Node

A typical node has two parts:

Data – the actual value stored in the node.

Next – a reference/pointer to the next node (or null if it’s the last node).

[Data | Next] -> [Data | Next] -> [Data | Next] -> null

 */