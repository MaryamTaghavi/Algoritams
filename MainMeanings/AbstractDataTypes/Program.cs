/* An Abstract Data Type (ADT) is a theoretical concept in computer science that defines
 a data structure by its behavior rather than its implementation. In simpler terms */

/*
It specifies what operations can be performed on the data and what those operations do, 
without specifying how the data is stored or how the operations are implemented. */

/*

Common Examples of ADTs:

Stack – Supports operations like push, pop, peek.
Queue – Supports enqueue, dequeue, front.
List – Supports operations like insert, delete, get.
Map/Dictionary – Supports put, get, remove.

*/